static int write_all(int fd, const uint8_t *buf, size_t len)
{
	ssize_t ret = write(fd, buf, len);

	if (ret == (ssize_t)len)
		return 0;

	fprintf(stderr, "write failed: ret=%zd len=%zu errno=%d (%s)\n",
		ret, len, errno, strerror(errno));
	return -1;
}

static int read_reg(int fd, uint8_t reg, uint8_t *value)
{
	if (write_all(fd, &reg, 1))
		return -1;
	if (read(fd, value, 1) != 1) {
		fprintf(stderr, "read reg 0x%02x failed: errno=%d (%s)\n",
			reg, errno, strerror(errno));
		return -1;
	}
	return 0;
}

static int write_reg(int fd, uint8_t reg, uint8_t value)
{
	uint8_t buf[2] = { reg, value };

	return write_all(fd, buf, sizeof(buf));
}

int main(int argc, char **argv)
{
	const char *dev = argc > 1 ? argv[1] : "/dev/i2c-3";
	uint8_t mvol = 0xff, ch1 = 0x30, ch2 = 0x30, mute = 0x00;
	int fd = open(dev, O_RDWR);
	size_t i;

	if (fd < 0) {
		fprintf(stderr, "open %s failed: errno=%d (%s)\n",
			dev, errno, strerror(errno));
		return 1;
	}
	if (ioctl(fd, I2C_SLAVE, 0x1b) < 0) {
		fprintf(stderr, "I2C_SLAVE 0x1b failed: errno=%d (%s)\n",
			errno, strerror(errno));
		return 1;
	}

	read_reg(fd, 0x07, &mvol);
	read_reg(fd, 0x08, &ch1);
	read_reg(fd, 0x09, &ch2);
	read_reg(fd, 0x06, &mute);

	for (i = 0; i < sizeof(tas5713_init) / sizeof(tas5713_init[0]); i++) {
		if (write_all(fd, tas5713_init[i].data, tas5713_init[i].len))
			return 1;
	}

	if (write_reg(fd, 0x05, 0x00) ||
	    write_reg(fd, 0x07, mvol) ||
	    write_reg(fd, 0x08, ch1) ||
	    write_reg(fd, 0x09, ch2) ||
	    write_reg(fd, 0x06, mute) ||
	    write_reg(fd, 0x02, 0x00))
		return 1;

	fprintf(stderr,
		"reapplied TAS5713 init; restored mvol=0x%02x ch1=0x%02x ch2=0x%02x mute=0x%02x\n",
		mvol, ch1, ch2, mute);
	close(fd);
	return 0;
}
